func fibonacci(n: Int) -> Int {
    
    if n <= 2 {
        print("Calculating fibonacci(\(n))...")
        print("Terminal case, returning 1.")
        return(1)
    } else {
        print("Calculating fibonacci(\(n))...")
        let y =  fibonacci(n: n-1) + fibonacci(n: n-2)
        print("Non-terminal case, returning \(y).")
        return(y)
    }   
}
print("The final result is: \(fibonacci(n: n)).")

