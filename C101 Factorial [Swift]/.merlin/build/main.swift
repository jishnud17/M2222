func factorial( n: Int) -> Int {
   
    if n <= 1 {
        print("Calculating factorial(\(n))...")
        print("Terminal case, returning 1.")
        return(1)
    }
    else{
        print("Calculating factorial(\(n))...")
        let y = n * factorial(n: n-1)
        print("Non-terminal case, returning \(y).")     
        return(y)
        
    }
   

}

print("The final result is: \(factorial(n: n)).")
